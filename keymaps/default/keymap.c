bool process_record_user(uint16_t keycode, keyrecord_t *record) {
		
	           /* KEYBOARD PET STATUS START */
		switch (keycode) {
			case KC_LCTL:
			case KC_RCTL:
				if (record->event.pressed) {
					isSneaking = true;
				} else {
					isSneaking = false;
				}
				break;
			case KC_SPC:
				if (record->event.pressed) {
					isJumping  = true;
					showedJump = false;
				} else {
					isJumping = false;
				}
				break;
				/* AUDIO BUTTIONS */
			case QMKSONG1:
				if (record->event.pressed) {
					// when keycode QMKSONG1 is pressed
					PLAY_SONG(song1);
				}
				break;
			case QMKSONG2:
				if (record->event.pressed) {
					// when keycode QMKSONG2 is pressed
					PLAY_SONG(song2);
				}
				break; 
			case QMKSONG3:
				if (record->event.pressed) {
					// when keycode QMKSONG3 is pressed
					PLAY_SONG(song3);
				}
				break;
			case QMKSONG4:
				if (record->event.pressed) {
					// when keycode QMKSONG4 is pressed
					PLAY_SONG(song4);
				}
				break;
		}
            /* KEYBOARD PET STATUS END */
	
	return true;
}

